SELECT EMP_NUM, EMP_LNAME, EMP_FNAME, EMP_INITIAL
FROM EMPLOYEE
WHERE EMP_LNAME LIKE "SMITH%"
ORDER BY EMP_NUM;



SELECT PROJ_NAME, PROJ_VALUE, PROJ_BALANCE,
       EMP_LNAME, EMP_FNAME, EMP_INITIAL,
       JOB_CODE, JOB_DESCRIPTION, JOB_CHG_HOUR
FROM PROJECT 
     JOIN EMPLOYEE USING (EMP_NUM)
     JOIN JOB USING (JOB_CODE)
ORDER BY PROJ_VALUE;



SELECT PROJ_NAME, PROJ_VALUE, PROJ_BALANCE,
       EMP_LNAME, EMP_FNAME, EMP_INITIAL,
       JOB_CODE, JOB_DESCRIPTION, JOB_CHG_HOUR
FROM PROJECT 
     JOIN EMPLOYEE USING (EMP_NUM)
     JOIN JOB USING (JOB_CODE)
ORDER BY EMP_LNAME;




